namespace Content.Client._Moffstation.Voting.Ui;

/// <summary>
/// Lets a player pick which character they'll spawn as for an enroll vote, mid-round, without going back to
/// the lobby. Picking one of their own just selects it like the lobby does - the antag spawn path already
/// reads the selected character. "Random" is per-vote, and is the only bit the server needs telling about.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class MoffCharacterSelectWindow : FancyWindow
{
    [Dependency] private IClientPreferencesManager _preferences = default!;
    [Dependency] private IPrototypeManager _proto = default!;
    [Dependency] private ISharedPlayerManager _player = default!;
    [Dependency] private IEntityManager _entityManager = default!;

    private static readonly SpriteSpecifier DiceSprite =
        new SpriteSpecifier.Rsi(new ResPath("/Textures/Objects/Fun/dice.rsi"), "d6_6");

    private readonly EntityUid _enroller;

    public MoffCharacterSelectWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public MoffCharacterSelectWindow(EntityUid enroller)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _enroller = enroller;

        DiceIcon.Texture = _entityManager.System<SpriteSystem>().Frame0(DiceSprite);

        // Toggling it back off just falls through to whichever character they have selected.
        RandomButton.OnPressed += args =>
        {
            SetRandom(args.Button.Pressed);
            Close();
        };

        ReloadCharacterPickers();
    }

    /// <summary>
    /// Disposes and rebuilds every picker button from the preferences data.
    /// </summary>
    private void ReloadCharacterPickers()
    {
        Characters.RemoveAllChildren();

        if (!_preferences.ServerDataLoaded || _preferences.Preferences is not { } preferences)
            return;

        var random = PrefersRandom();
        RandomButton.Pressed = random;

        var group = new ButtonGroup();
        var selectedSlot = preferences.SelectedCharacterIndex;

        foreach (var (slot, character) in preferences.Characters)
        {
            // While random is picked, none of their own characters is what they'd spawn as.
            var button = new CharacterPickerButton(_proto, _player, group, character, !random && slot == selectedSlot);

            // This picker only picks; managing characters stays in the lobby.
            button.DeleteButton.Visible = false;

            Characters.AddChild(button);

            button.OnPressed += _ =>
            {
                _preferences.SelectCharacter(slot);
                SetRandom(false);
                Close();
            };
        }
    }

    /// <summary>
    /// Whether this player already asked to spawn as a randomly generated character for this vote.
    /// </summary>
    private bool PrefersRandom()
    {
        return _entityManager.TryGetComponent<MoffEnrollEventComponent>(_enroller, out var comp)
               && _entityManager.GetNetEntity(_player.LocalEntity) is { } netEntity
               && comp.RandomPick.Contains(netEntity);
    }

    private void SetRandom(bool random)
    {
        var netEnroller = _entityManager.GetNetEntity(_enroller);
        _entityManager.RaisePredictiveEvent(new MoffSetEnrollRandomMessage(netEnroller, random));
    }
}
